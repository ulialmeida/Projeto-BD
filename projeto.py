import sqlite3

# Conecta ao banco de dados SQLite
conn = sqlite3.connect('compounds.db')
cursor = conn.cursor()

# Cria tabela no banco de dados | fazendo referência ao nome da tabela
cursor.execute('''
    CREATE TABLE IF NOT EXISTS tbl_compound (
        compound_id INTEGER PRIMARY KEY,
        compound TEXT,
        molecular_formula TEXT,
        molecular_mass REAL
    )
''')

cursor.execute('''
    CREATE TABLE IF NOT EXISTS tbl_matrixes (
        matrixes_id INTEGER PRIMARY KEY,
        organism TEXT,
        plant_tissue TEXT
    )
''')

cursor.execute('''
    CREATE TABLE IF NOT EXISTS tbl_name (
        name_id INTEGER PRIMARY KEY,
        name TEXT
    )
''')

cursor.execute('''
    CREATE TABLE IF NOT EXISTS tbl_identification (
        compound_id INTEGER,
        matrix_id INTEGER,
        name_id INTEGER,
        FOREIGN KEY (compound_id) REFERENCES tbl_compound(compound_id),
        FOREIGN KEY (matrix_id) REFERENCES tbl_matrixes(matrixes_id),
        FOREIGN KEY (name_id) REFERENCES tbl_name(name_id)
    )
''')

# Crie a tabela de usuário e senha
cursor.execute('''
    CREATE TABLE IF NOT EXISTS tbl_users (
        id INTEGER PRIMARY KEY,
        username TEXT UNIQUE,
        password TEXT
    )
''')

# Para inserção de dados em tbl_matrixes
def inserir_dados_matrixes(organism, plant_tissue):
    cursor.execute("INSERT INTO tbl_matrixes (organism, plant_tissue) VALUES (?, ?)",
                   (organism, plant_tissue))
    conn.commit()

# Função para inserção de dados em tbl_compound
def inserir_dados(compound, molecular_formula, molecular_mass):
    cursor.execute("INSERT INTO tbl_compound (compound, molecular_formula, molecular_mass) VALUES (?, ?, ?)",
                   (compound, molecular_formula, molecular_mass))
    conn.commit()

# Função para inserção de dados em tbl_name
def inserir_dados_name(name):
    cursor.execute("INSERT INTO tbl_name (name) VALUES (?)", (name,))
    conn.commit()

# Função para inserção de dados em tbl_identification
def inserir_dados_identification(compound_id, matrixes_id, name_id):
    cursor.execute("INSERT INTO tbl_identification (compound_id, matrix_id, name_id) VALUES (?, ?, ?)",
                   (compound_id, matrixes_id, name_id))
    conn.commit()

# Função para inserção de dados em tbl_users
def inserir_dados_usuario(username, password):
    cursor.execute("SELECT * FROM tbl_users WHERE username = ?", (username,))
    existing_user = cursor.fetchone()
    
    if existing_user is None:
        cursor.execute("INSERT INTO tbl_users (username, password) VALUES (?, ?)", (username, password))
        conn.commit()
    else:
        print("Usuário já existe no banco de dados.")
    
    conn.commit()

